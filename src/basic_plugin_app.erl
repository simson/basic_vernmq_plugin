-module(basic_plugin_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
	application:ensure_all_started(inets),
    basic_plugin_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

